def annotate_dc_extrapolation(beta_hat, alpha_hat, n_extrap, residual_var, conf=0.95):
    import math
    from scipy import stats
    t_pred_s = (10 ** alpha_hat) * (n_extrap ** beta_hat)
    t_pred_min = t_pred_s / 60.0
    log_n = math.log10(n_extrap)
    se_log_t = math.sqrt(residual_var * (1 + log_n ** 2))
    z = stats.norm.ppf((1 + conf) / 2)
    lo_min = (10 ** (math.log10(t_pred_s) - z * se_log_t)) / 60.0
    hi_min = (10 ** (math.log10(t_pred_s) + z * se_log_t)) / 60.0

    def fmt(x):
        if x >= 1000:
            return f"{x:,.0f}"
        elif x >= 10:
            return f"{x:.0f}"
        else:
            return f"{x:.1f}"

    return f"{fmt(t_pred_min)} [{fmt(lo_min)}, {fmt(hi_min)}] min **"

def collect_system_state():
    state = {
        "generated_at": datetime.datetime.utcnow().isoformat() + "Z",
        "python_version": platform.python_version(),
        "platform": platform.platform(),
        "processor": platform.processor(),
    }
    for pkg in ["numpy", "scipy", "scikit-learn", "dcor", "minepy", "yaml"]:
        try:
            import importlib
            mod = importlib.import_module(pkg.replace("-", "_").split(".")[0])
            state[f"pkg_{pkg}"] = getattr(mod, "__version__", "unknown")
        except ImportError:
            state[f"pkg_{pkg}"] = "not installed"
    try:
        import numpy as np
        blas_info = str(np.__config__.blas_opt_info)
        state["blas_backend"] = "MKL" if "mkl" in blas_info.lower() else "OpenBLAS/other"
    except Exception:
        state["blas_backend"] = "unknown"
    try:
        import hashlib
        with open("config.yaml", "rb") as f:
            state["config_yaml_sha256"] = hashlib.sha256(f.read()).hexdigest()
    except Exception:
        state["config_yaml_sha256"] = "error"
    return state

